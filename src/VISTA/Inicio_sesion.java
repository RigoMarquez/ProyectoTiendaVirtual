    public boolean validacioninicio() {
        // OBTENER EL USUARIO Y LA CONTRASEÑA
        String contrasena = txtcontraseña.getText();
        String usuarioIngresado = txtusuario.getText().trim(); //El trim() se utiliza aquí para eliminar espacios en blanco al inicio y al final del texto ingresado 

        try {
            objPro.leer();
            // VERIFICA SI LA CONTRASEÑA ESTA EN EL ARCHIVO Y SI ESTA CORRESPONDE CON EL USUARIO
            for (Usuario u : objPro.lista) {
                if (u.getUsuario().trim().equals(usuarioIngresado) && u.getContraseña().equals(contrasena)) {
                    return true;
                }
            }

        } catch (IOException e) {
        }
        return false;
    }

